export function getOpportunityBySlug(slug: string): Opportunity | undefined {
  return opportunities.find((o) => o.slug === slug)
}

export function getFeaturedOpportunities(count = 6): Opportunity[] {
  const featured = opportunities.filter((o) => o.featured)
  return featured.length >= count
    ? featured.slice(0, count)
    : [...featured, ...opportunities.filter((o) => !o.featured)].slice(0, count)
}

export function getOpportunitiesByType(type: string): Opportunity[] {
  return opportunities.filter((o) => o.type === type)
}

export function getOpportunitiesByCountry(countryCode: string): Opportunity[] {
  return opportunities.filter((o) => o.countries.includes(countryCode))
}
